public class Square {
    public static void main(String[] args) {
        /* 사각형의 둘레와 넓이는 구하는 프로그램 */
        int w = 3, h = 10;
        System.out.println("사각형의 가로는 " + w);
        System.out.println("사각형의 세로는 " + h);
        System.out.println("사각형의 둘레는 " + (w + h) * 2);
        System.out.println("사각형의 넓이는 " + w * h);        
    }
}